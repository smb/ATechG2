package de.adv.atech.roboter.gui.commandparser;

public class Loop {

	private int commandListIndex;

	private int loopLength;

	public int getCommandListIndex() {
		return commandListIndex;
	}

	public void setCommandListIndex(int commandListIndex) {
		this.commandListIndex = commandListIndex;
	}

	public int getLoopLength() {
		return loopLength;
	}

	public void setLoopLength(int loops) {
		this.loopLength = loops;
	}

}
